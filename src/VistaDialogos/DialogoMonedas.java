    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        // TODO add your handling code here:
        if (txtBuscar == evt.getSource()) {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                String Opcion = "0", sfieldName = "";

                if (jRInicio.isSelected()) {
                    Opcion = "0";
                } else if (jRFinal.isSelected()) {
                    Opcion = "1";
                } else if (jrCualquier.isSelected()) {
                    Opcion = "2";
                } else if (jRexacta.isSelected()) {
                    Opcion = "3";
                }
                sfieldName = (jrCodigo.isSelected()) ? "Co_mone" : "mone_des";
                cargarDatosMonedas(Vistaprincipal.lblNombreEmpresa.getText() , "samoneda", sfieldName, txtBuscar.getText(),Opcion);
            }
        }
    }//GEN-LAST:event_txtBuscarKeyReleased

