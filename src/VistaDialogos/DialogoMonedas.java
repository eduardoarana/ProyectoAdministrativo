    private void btnAceptarMonedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarMonedaActionPerformed
        // TODO add your handling code here:
        int selection = jtableMonedas.getSelectedRow();
        if (selection == -1) {
            JOptionPane.showMessageDialog(new JFrame(), "Debe seleccionar una Moneda", "Software", JOptionPane.INFORMATION_MESSAGE);
            return;
        } else {
            
            VistaAjustesES.txtMoneda.setText(jtableMonedas.getValueAt(selection,0).toString().replaceAll("\\s","")); 
//            DialogoBusquedaTabla.txtCodLinea.setText(jtableMonedas.getValueAt(selection, 0).toString().replaceAll("\\s", ""));
//            DialogoBusquedaTabla.txtDescripcionLinea.setText(jtableMonedas.getValueAt(selection, 1).toString().replaceAll("\\s", ""));
            dispose();
        }
    }//GEN-LAST:event_btnAceptarMonedaActionPerformed

