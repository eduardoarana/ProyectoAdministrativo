    private void btnAceptarjDialogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarjDialogActionPerformed
        // TODO add your handling code here:
        int selectionRoow = jtablaAlmacen.getSelectedRow();
        if (selectionRoow != -1){

            String codAlmacen = jtablaAlmacen.getValueAt(selectionRoow, 0).toString();

            //validar de donde viene elevento "Modulo Ventas  ,Modulo de Compras , Modulo de Inventario AES ," 
            if (Utilitarios.Utilitario.MODULO_FACTURA_VENTAS.equals("FACTURAVENTA")) {
                facturaVenta.jTablaFacturaVentaRenglon.setValueAt(codAlmacen.toString(), facturaVenta.FILA_jtablaventasreng, 3);
            } else if (Utilitarios.Utilitario.MODULO_FACTURA_VENTAS.equals("AJUSTESES")) {
                ajustesES.jtableAjusteES.setValueAt(codAlmacen.toString(), ajustesES.FILA_jtableAjusteES, 5);
            } else if (Utilitarios.Utilitario.MODULO_FACTURA_VENTAS.equals("FACTURACOMPRA")) {
                facturaCompras.jTablaFacturaCompraRenglon.setValueAt(codAlmacen.toString(), facturaCompras.FILA_jtablaComprasreng, 3);
            } else if (Utilitarios.Utilitario.MODULO_COTIZACION_VENTAS.equals("COTIZACIONVENTA2")) {
                System.out.println("cotizacionVenta.FILA_jtablaCotizacionReng " +cotizacionVenta.FILA_jtablaCotizacionReng);
                cotizacionVenta.jTablaCotizacionRenglon.setValueAt(codAlmacen.toString(), cotizacionVenta.FILA_jtablaCotizacionReng, 3);
            }
            dispose();
        }
    }//GEN-LAST:event_btnAceptarjDialogActionPerformed

    private void txtBuscarValorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarValorKeyReleased
        // TODO add your handling code here:        
        String Opcion = "0", sfieldName = "";

        if (jRInicio.isSelected()) {
            Opcion = "0";
        } else if (jRFinal.isSelected()) {
            Opcion = "1";
        } else if (jrCualquier.isSelected()) {
            Opcion = "2";
        } else if (jRexacta.isSelected()) {
            Opcion = "3";
        }
        sfieldName = (jrCodigo.isSelected()) ? "co_alma" : "des_alma";
        cargarDatosAlmacen(Vistaprincipal.tempEmpresa, "saAlmacen", sfieldName, txtBuscarValor.getText(), Opcion);
    }//GEN-LAST:event_txtBuscarValorKeyReleased

