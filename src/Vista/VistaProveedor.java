    private void txtvalorTipoproveedorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtvalorTipoproveedorKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String Opcion1 = "0", sfieldName1 = "";

            if (jRInicio1.isSelected()) {
                Opcion1 = "0";
            } else if (jRFinal1.isSelected()) {
                Opcion1 = "1";
            } else if (jrCualquier1.isSelected()) {
                Opcion1 = "2";
            } else if (jRexacta1.isSelected()) {
                Opcion1 = "3";
            }
            sfieldName1 = (jrCodigo1.isSelected()) ? "tip_pro" : "des_tipo";
            jtablaTipo.setModel(new ModeloTablaTipoProveedor(tipoModelo.pObtenerListadoDatosProveedor(
                    "admin_a",
                    "saTipoProveedor",
                    sfieldName1,
                    txtvalorTipoproveedor.getText(),
                    Opcion1)
            ));
        }
    }//GEN-LAST:event_txtvalorTipoproveedorKeyPressed

