    private void btnAceptarSubLineaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarSubLineaActionPerformed
        // TODO add your handling code here:
        int selection = jtableSubLinea.getSelectedRow();
        if (selection != -1) {
            DialogoBusquedaTabla.txtCodSublinea.setText(jtableSubLinea.getValueAt(selection, 0).toString().replaceAll("\\s", ""));
            DialogoBusquedaTabla.txtDescripcionSublinea.setText(jtableSubLinea.getValueAt(selection, 1).toString().replaceAll("\\s", ""));
            dispose();
        } else {
            JOptionPane.showMessageDialog(new JFrame(), "Debe Seleccionar una linea", "Software", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAceptarSubLineaActionPerformed
    public void pObtenerListadoDatosSubLinea(String sDatabase_Name, String sTable_Name, String sField_Name, String sValor, String iOpcion, String sCo_lin) {
        ArrayList<SubLineaBO> lista = new SublineaModel().pObtenerListadoDatosSubLinea(sDatabase_Name, sTable_Name, sField_Name, sValor, iOpcion, sCo_lin);
        if (lista.size() == 0) {
            JOptionPane.showMessageDialog(new JFrame(), "No se encontraron sublineas para este articulo", "Software", JOptionPane.INFORMATION_MESSAGE);
            return;
        } else if (lista.size() != 0) {
            jtableSubLinea.setModel(new ModeloTablaSubLinea(lista));
        }

    }

