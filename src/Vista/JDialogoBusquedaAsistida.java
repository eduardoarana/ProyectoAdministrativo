    private void txtBuscarValorSubLineaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarValorSubLineaKeyReleased
        // TODO add your handling code here:
        if (txtBuscarValorSubLinea == evt.getSource()) {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                String Opcion4 = "0", sfieldName4 = "";
                if (jRInicio4.isSelected()) {
                    Opcion4 = "0";
                } else if (jRFinal4.isSelected()) {
                    Opcion4 = "1";
                } else if (jrCualquier4.isSelected()) {
                    Opcion4 = "2";
                } else if (jRexacta4.isSelected()) {
                    Opcion4 = "3";
                }
                sfieldName4 = (jrCodigo4.isSelected()) ? "co_subl" : "subl_des";
                pObtenerListadoDatosSubLinea("admin_a", "saSubLinea", sfieldName4, txtBuscarValorSubLinea.getText(), Opcion4, DialogoBusquedaTabla.txtCodLinea.getText());
            }
        }
    }//GEN-LAST:event_txtBuscarValorSubLineaKeyReleased

