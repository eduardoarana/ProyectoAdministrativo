public class ArticuloBO {

    private String co_art;
    private String imagen;
    private String artDescr;
    private String categoria;
    private String Detal;
    private String disponibleDetal;
    private String Mayor;
    private String disponibleMayor;

    public ArticuloBO() {

    }

    public String getCo_art() {
        return co_art;
    }

    public void setCo_art(String co_art) {
        this.co_art = co_art;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public String getArtDescr() {
        return artDescr;
    }

    public void setArtDescr(String artDescr) {
        this.artDescr = artDescr;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getDetal() {
        return Detal;
    }

    public void setDetal(String Detal) {
        this.Detal = Detal;
    }

    public String getDisponibleDetal() {
        return disponibleDetal;
    }

    public void setDisponibleDetal(String disponibleDetal) {
        this.disponibleDetal = disponibleDetal;
    }

    public String getMayor() {
        return Mayor;
    }

    public void setMayor(String Mayor) {
        this.Mayor = Mayor;
    }

    public String getDisponibleMayor() {
        return disponibleMayor;
    }

    public void setDisponibleMayor(String disponibleMayor) {
        this.disponibleMayor = disponibleMayor;
    }

}
